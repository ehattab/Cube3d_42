float	spawn_angle(char p)
{
	if (p == 'N')
		return (3 * PI / 2);
	else if (p == 'S')
		return (PI / 2);
	else if (p == 'E')
		return (0);
	else if (p == 'W')
		return (PI);
	return (0);
}
